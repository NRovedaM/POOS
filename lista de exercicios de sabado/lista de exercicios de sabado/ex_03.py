#Desenvolva um programa que leia dois números inteiros e exiba a soma, a subtração e a multiplicação deles. (Lembre-se de converter os valores lidos com int().
num1 = int(input("insira um numero: "))
num2 = int(input("insira o segundo numero"))

print(f"o resultado da soma dos numeros é {num1 + num2}")
print(f"o resultado da subtração dos numeros é {num1 - num2}")
print(f"o resultado da multiplicação deles é {num1 * num2}")